    {
        $this->validate($request, [
            'description' => 'required',


        ]);
        $review = new Review();
        $review->description = $request->get('description');
        $review->rating =0;
        $review->user_id = Auth::id();
        $review->hotel_id=$request->get('hotel_id');

        $review->save();


        return redirect()->back();
    }

